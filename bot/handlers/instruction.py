"""Обработчик инструкции."""

from aiogram import Router, F
from aiogram.types import CallbackQuery

from keyboards import back_menu_kb

router = Router()

INSTRUCTION_TEXT = """
📖 <b>Инструкция по использованию</b>

━━━━━━━━━━━━━━━━━━

<b>1. Получение API ID и API Hash</b>

1️⃣ Перейдите на <a href="https://my.telegram.org">my.telegram.org</a>
2️⃣ Войдите по номеру телефона
3️⃣ Перейдите в <b>API Development Tools</b>
4️⃣ Создайте приложение (если ещё не создано)
5️⃣ Скопируйте <b>App api_id</b> и <b>App api_hash</b>

━━━━━━━━━━━━━━━━━━

<b>2. Привязка аккаунта</b>

1️⃣ В главном меню нажмите «📋 Мои аккаунты»
2️⃣ Нажмите «➕ Добавить аккаунт»
3️⃣ Введите API ID, API Hash и номер телефона
4️⃣ Введите код подтверждения из Telegram
5️⃣ Если есть 2FA — введите пароль

━━━━━━━━━━━━━━━━━━

<b>3. Создание рассылки</b>

1️⃣ Нажмите «🚀 Начать рассылку»
2️⃣ Выберите аккаунт для отправки
3️⃣ Введите текст или выберите шаблон
4️⃣ Введите юзернеймы или выберите список
5️⃣ Настройте задержку и перерывы
6️⃣ Подтвердите и запустите

━━━━━━━━━━━━━━━━━━

<b>4. Шаблоны и списки</b> (Pro)

🗂 <b>Шаблоны</b> — сохраните часто используемые тексты
📋 <b>Списки</b> — сохраните группы юзернеймов

Переменные в тексте:
• <code>{username}</code> — юзернейм получателя
• <code>{first_name}</code> — имя получателя

━━━━━━━━━━━━━━━━━━

<b>5. Подписка и оплата</b>

💎 <b>Pro — $2/мес</b>
• Безлимит сообщений
• Настройка задержек и перерывов
• Шаблоны, списки, отложенные рассылки
• До 5 аккаунтов

🆓 <b>Free</b>
• 10 сообщений в день
• Задержка 5 минут
• 1 аккаунт

Оплата через @CryptoBot (USDT, BTC, ETH и др.)

━━━━━━━━━━━━━━━━━━

<b>6. FAQ</b>

❓ <b>Что делать при спам-блоке?</b>
Бот автоматически обращается к @SpamBot. Если блок не снят — рассылка останавливается. Подождите несколько часов.

❓ <b>Безопасно ли это?</b>
Бот использует вашу сессию только для рассылки. Пароли 2FA не сохраняются. Рекомендуется не превышать 50-100 сообщений в день.

❓ <b>Почему сообщение не доставлено?</b>
Причины: приватность получателя, блокировка, деактивированный аккаунт, ограничение на сообщения за звёзды.
"""


@router.callback_query(F.data == "instruction")
async def cb_instruction(callback: CallbackQuery):
    await callback.message.edit_text(
        INSTRUCTION_TEXT,
        reply_markup=back_menu_kb(),
        parse_mode="HTML",
        disable_web_page_preview=True
    )
    await callback.answer()
